<div class="container">
    <h3 class="titre-section">Processus de labellisation</h3>
    <div class="row phaseTete">
        <h3 class="sous-titre-section">Contextualisation</h3>
        <div class="textPhase">
            <p>
                <b>Enregistrement et Labellisation des PME et startups du secteur du numérique.</b>
                Cette initiative se veut une contribution à l’opérationnalisation de l’axe 3 de la <b>Stratégie SN 2025</b> relatif à la Promotion d’une Industrie du numérique innovante et créatrices de valeurs. Elle prend en charge les préoccupations formulées par les acteurs du secteur certains partenaires comme la Banque mondiale dans le cadre de l’appui budgétaire
                L’objectif visé est de <b>renforcer la politique d'accompagnement</b> à l’entreprenariat et de soutien à l'innovation du Ministère.
                Ce processus, qui sera mené de façon inclusive et participative, se déroule en <span class="font-weight-bold">quatre étapes</span> marquées successivement par l’identification, l’enregistrement, l’’évaluation et la reconnaissance/accréditation des PME/startups
            </p>
        </div>
    </div>
    <div class="row phase">
        <div class="imgPhase"></div>
        <div class="textPhase">
            <h3 class="sous-titre-section">Phase d'Identification</h3>
            <p class="description">
                Elle consacre le démarrage du processus et permettra, à terme, de recenser l’ensemble des PME, startups et porteurs de projets du secteur du numérique qui pourront faire l’objet d’un enregistrement.
                Cette phase a déjà démarré avec :
                <ul>
                    <li>L’élaboration d’une plateforme d’identification permettant de recueillir les informations relatives à l’existence et aux activités des startups. Elle s’appuie sur un échantillon d’une centaine de startups et de PME déjà identifiées et représentatives de l’écosystème.</li>
                </ul>
                Elle se poursuivra par :
                <ul>
                    <li>L’administration du questionnaire à cet échantillon de PME/startups préalablement identifiées</li>
                    <li>La mise en ligne de la plateforme de recensement .</li>
                </ul>
                Le Recueil et l’exploitation des retours de questionnaires aboutira à la production d’une fiche d’identisation qui décrit le profil de chaque startup et d’un rapport d’identification qui dresse une cartographie des PME/startups du secteur.
            </p>
        </div>
    </div>
    <div class="row phase">
        <div class="imgPhase"></div>
        <div class="textPhase">
            <h3 class="sous-titre-section">Phase d'Enregistrement</h3>
            <p class="description">
                C’est la suite logique de la phase d’identification avec pour objectif de rendre lisible l’écosystème des PME/startups.
                Elle consistera en :
                <ul>
                    <li>La définition et la validation des critères d’enregistrement objectifs : justifier d’une existence physique et juridique, d’une activité permanente.</li>
                    <li>Le contrôle et la vérification de ces critères pour chaque startup via des demandes de renseignement supplémentaires (NINEA, RC) et des visites de sites si nécessaires (siège, locaux des startups, etc.). Les startups et PME n’ayant pas été retenues seront invitées à se formaliser avant d’être enregistrées.</li>
                    Cette phase se traduira par :
                    <li>La Délivrance d’une attestation/quitus d’enregistrement signée par le Ministre aux startups ayant satisfait les critères d’existence.</li>
                    <li>La mise en place d’une base de données des PME/startups classées par secteurs d’activités, situation géographique ou forme juridique.</li>
                </ul>
            </p>
        </div>
    </div>
    <div class="row phase">
        <div class="imgPhase"></div>
        <div class="textPhase">
            <h3 class="sous-titre-section">Phase d'Evaluation Technique</h3>
            <p class="description">
                Elle sera marquée par :
                <ul>
                    <li>La mise en place officielle d’un comité d’évaluation composé de toutes les parties prenantes concernées par le processus d’évaluation.</li>
                    <li>La définition et validation des critères d’évaluation permettant de jauger le niveau de maturité, le stade de développement, le caractère innovant, le modèle économique et l’impact économique et social de chaque PME/startup</li>
                    <li>L’évaluation des PME/startup au regard des critères définis par diverses méthodes : revue documentaire (fiche d’identification, base de données, rapport d’évaluation, etc.) entretien, audition des startups (présentation de solutions, démos et tests, etc.)</li>
                </ul>
                Cette phase sera sanctionnée par :
                <ul>
                    <li>La sélection des PME/startups retenues à l’issue de la phase d’évaluation.</li>
                    <li>La production d’un rapport d’évaluation et de délibération indiquant les PME/startups retenues pour être labellisées.</li>
                    <li>La constitution d’une base d’information et d’aide à la prise de décision en matières d’appui et d’accompagnement aux PME/startups.</li>
                </ul>
            </p>
        </div>
    </div>
    <div class="row phase">
        <div class="imgPhase"></div>
        <div class="textPhase">
            <h3 class="sous-titre-section">Phase de reconnaissance et labellisation institutionnelle</h3>
            <p class="description">
                Cette phase couronne le processus et sera marquée par :
                <ul>
                    <li>La délivrance d’un titre de labellisation signé par le Ministre et valable pour une durée de cinq ans renouvelable aux PME/startups retenues ;</li>
                    <li>La mise en place d’un portail national de promotion des /PME/startups labellisées ;</li>
                    <li>La définition et mise à disposition d’un paquet d’opportunités ouverts aux startups/PME labellisées (participation aux évènements du Ministère et de ses partenaires, appels à projets restreints ; accès aux PTN, accès aux Fonds du numérique, Financement DER, accès à commande publique, soutien à la Protection intellectuelle etc.).</li>
                </ul>
                Dans les jours à venir, nous vous soumettrons pour validation :
                <ul>
                    <li>Un modèle d’attestation d’enregistrement à délivrer à toute startup/PME identifiée et qui satisfait aux critères d’existence juridique et physique ;</li>
                    <li>Un projet d’arrêté portant mise en place d’un comité d’évaluation technique chargé de d’évaluer le niveau de maturité, le caractère innovant, le modèle économique et l’impact économique et social des PME/startup déjà enregistrées</li>
                    <li>Un modèle de titre de labellisation à délivrer aux startup/PME retenues après la phase d’évaluation.</li>
                    <li>Une offre d’accompagnement à proposer aux startups/PME labellisées.</li>
                </ul>
            </p>
        </div>
    </div>
</div>